public class Shake {
    private String baseIng1;
    private String baseIng2;

    public Shake(){}

    public Shake(String baseIng1, String baseIng2) {
        this.baseIng1 = baseIng1;
        this.baseIng2 = baseIng2;
    }

    public String getBaseIng1() {
        return baseIng1;
    }

    public String getBaseIng2() {
        return baseIng2;
    }
}
